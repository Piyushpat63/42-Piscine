#include <unistd.h>

void	rush01(int x, int y)
{
	int	i = x;
	int	j = y;
	
	i = 1;
	j = 1;
	while(j <= y)
	{
		i = 1;
		while(i <= x)
		{
			if (((i == 1) && (j == 1)) || ((i == x) && (j == y) && (i != 1) && (y != 1) ))
			{
				ft_putchar('/');
			}	
			else if(((i == 1) && (j == y)) || ((i == x) && (j == 1)))
			{
				ft_putchar('\\');
			}	
			else if(((i == 1) && (j > 1) && (j < y)) || ((j > 1) && (i == x) && (j < y)))
			{
				ft_putchar('*');
		 	}	
			else if (((j == 1) && (i > 1) && (i < x)) || ((i > 1) && (j == y) && (i < x)))
			{
				ft_putchar('*');
			}
			else
			{	
				ft_putchar(' ');
			}	
			i++;
		}
		ft_putchar('\n');
		j++;
	}	
}
